namespace Bools
{
    public static class LogicalPuzzles
    {
        public static bool Puzzle1(bool b1, bool b2)
        {
            // TODO #3-1. Solve the puzzle by using logical operators only.
            return !b1 || b2;
        }

        public static bool Puzzle2(bool b1, bool b2)
        {
            // TODO #3-2. Solve the puzzle by using logical operators only.
            return !b1 && b2;
        }

        public static bool Puzzle3(bool b1, bool b2)
        {
            // TODO #3-3. Solve the puzzle by using logical operators only.
            return b1 || !b2;
        }

        public static bool Puzzle4(bool b1, bool b2)
        {
            // TODO #3-4. Solve the puzzle by using logical operators only.
            return b1 && !b2;
        }

        public static bool Puzzle5(bool b1, bool b2)
        {
            // TODO #3-5. Solve the puzzle by using logical operators only.
            return !(b1 || b2);
        }

        public static bool Puzzle6(bool b1, bool b2)
        {
            // TODO #3-6. Solve the puzzle by using logical operators only.
            return !(b1 && b2);
        }

        public static bool Puzzle7(bool b1, bool b2)
        {
            // TODO #3-7. Solve the puzzle by using logical operators only.
            return (b1 && b2) || !(b1 || b2);
        }
    }
